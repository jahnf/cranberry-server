#ifndef CHAR_DEFINES_H_
#define CHAR_DEFINES_H_

#define CHAR_CR                     '\r'
#define CHAR_LF                     '\n'
#define CHAR_LF_Translated          '\n'
#define CHAR_CRLF                   "\r\n"
#define CHAR_SQ_OPEN_Translated     '['
#define CHAR_SQ_CLOSE_Translated    ']'
#define CHAR_SQ_OPEN                '['
#define CHAR_SQ_CLOSE               ']'

#define ASCII_LF        0x0A /* '\n' */
#define ASCII_CR        0x0D /* '\r' */
#define ASCII_TAB       0x09 /* '\t' */
#define ASCII_DOLLAR    0x24 /* '$'  */
#define ASCII_DBLQUOTE  0x22 /* '"'  */
#define ASCII_BACKSLASH 0x5c /* '\\' */
#define ASCII_LT        0x3c /* '<'  */
#define ASCII_GT        0x3e /* '>'  */
#define ASCII_AMP       0x26 /* '&'  */
#define ASCII_N         0x6e /* 'n'  */
#define ASCII_T         0x74 /* 't'  */
#define ASCII_SPACE     0x20 /* ' '  */
#define ASCII_SLASH     0x2F /* '/'  */
#define ASCII_QMARK     0x3F /* '?'  */
#define ASCII_EQUAL     0x3D /* '='  */
#define ASCII_UNDERSCR  0x5F /* '_'  */
#define ASCII_DOT       0x2E /* '.'  */
#define ASCII_DBLPOINT  0x3A /* ':'  */
#define ASCII_SEMICOLN  0x3B /* ';'  */
#define ASCII_PERCENT   0x25 /* '%'  */
#define ASCII_PLUS      0x2B /* '+'  */
#define ASCII_COMMA     0x2C /* ','  */
#define ASCII_SQB_OPEN  0x5B /* '['  */
#define ASCII_SQB_CLOSE 0x5D /* ']'  */
#define ASCII_CB_OPEN   0x7B /* '{'  */
#define ASCII_CB_CLOSE  0x7D /* '}'  */

#define ASCII_CRLF      "\x0D\x0A"  /* "\r\n" */

#endif /* CHAR_DEFINES_H_ */
